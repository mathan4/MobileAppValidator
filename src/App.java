class App {
    String appName;
    String developerName;
    int appSizeInMB;
    String category;
    int price;

    public App(String appName, String developerName, int appSizeInMB, String category, int price) {
        this.appName = appName;
        this.developerName = developerName;
        this.appSizeInMB = appSizeInMB;
        this.category = category;
        this.price = price;
    }
}